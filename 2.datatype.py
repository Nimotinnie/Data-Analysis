#布林值(可以任意寫 不需引號)
True
False
#有順序、可動的列表 List [用中括號] 用逗號隔開不同的字串，裝成ㄧ個集合/列表
[3,4,5]
["hello", "world"]
#有順序、不可移動的列表 Tuple （用小括號）
(3,4,5)
("Hello", "world")
#集合 Set {大括號} 無順序
{3,4,5}
{"Hello", "world"}
#字典 Dictionary 鍵和值的配對 冒號
{"apple" : "蘋果", "data" : "資料"}
